public class Kirby extends Cucumber
{
    /*
      This is a Kirby Cucumber, a subclass of the Cucumber class. It defines the properties that are strictly for Kirby Cucumbers.

      The abbreviated name for an Kirby cucumber is "Kr". 
      Its nutrition is 10, which means after each day, its value grows by 10.
      Its ripe duration is 3, which means it takes 3 weeks to fully mature.
     */
    
    public Kirby()  // default constructor
    {
	name = "Kr";//sets name to "Kr"
	nutrition = 10;//sets nutrition to 10
	ripeDuration = 3;//sets ripeDuration to 3
    }

} // end Kirby class
	
