public class English extends Cucumber
{
    /*
      This is an English Cucumber, a subclass of the Cucumber class. It defines the properties that are strictly for English Cucumbers.

      The abbreviated name for an English cucumber is "Eng". 
      Its nutrition is 5, which means after each day, its value grows by 5.
      Its ripe duration is 2, which means it takes 2 weeks to fully mature.
     */
    
    public English()  // default constructor
    {
	name = "Eng";
	nutrition = 5;
	ripeDuration = 2;
    }

} // end class English
	
