public class Armenian extends Cucumber
{
    /*
      This is an Armenian Cucumber, a subclass of the Cucumber class. It defines the properties that are strictly for Armenian Cucumbers.

      The abbreviated name for an Armenian cucumber is "Ar". 
      Its nutrition is 20, which means after each day, its value grows by 20.
      Its ripe duration is 5, which means it takes 5 weeks to fully mature.
     */
    
    public Armenian()  // default constructor
    {
	name = "Ar";//sets name to "Ar"
	nutrition = 20;//sets nutrition to 20
	ripeDuration = 5;//sets ripeDuration to 5
    }

} // end class Armenian
	
